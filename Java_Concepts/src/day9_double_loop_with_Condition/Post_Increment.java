package day9_double_loop_with_Condition;

public class Post_Increment {

	public static void main(String[] args) {
		
//		In post increment firstly it ll give the value of a to b than it ll increment in the a by 1 
//		 So b ll 13 and a ll 14
		int a= 13;
		int b=a++;//(post increment )
		System.out.println(b);
		System.out.println(a);

	}

}
