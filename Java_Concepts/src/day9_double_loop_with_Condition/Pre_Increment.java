package day9_double_loop_with_Condition;

public class Pre_Increment {

	public static void main(String[] args) {
		
//		In pre  increment firstly it ll add the value of a by 1  than it ll assign the value to b 
//		 So b ll 14 and a ll 14
		int a = 13;
		int b = ++a;// (pre increment )
		System.out.println(b);
		System.out.println(a);

	}

}
