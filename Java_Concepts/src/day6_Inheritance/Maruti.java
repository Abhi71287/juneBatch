package day6_Inheritance;

public class Maruti {
	
	public Maruti() {
		System.out.println("Parent Class Default Constructor");
	}
	
	public Maruti(int a) {
		System.out.println("Parent Class One Parameterized Constructor");
	}
	
	public Maruti(int a , int b) {
		System.out.println("Parent Class Two Parameterized Constructor");
	}
	
	public void method1() {
		System.out.println("Parent Class Method 1");
	}
	
	public void method2() {
		System.out.println("Parent Class Method 2");
	}

}
